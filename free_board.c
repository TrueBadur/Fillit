#include "fillit.h"

void	free_board(t_board **b)
{
	int		i;
	char	**d;

	i = -1;
	d = (*b)->data;
	while (++i < (*b)->w)
		free((*b)->data[i]);
	free(*b);
	b = NULL;
}
